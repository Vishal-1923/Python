# Operators:
# + : add
# - : subtract
# * : multiply
# / : divide
# // : floor division
# ** : exponential

print("Sum: ", 5 + 6)
print("Sub: ", 15 - 6)
print("MUL: ", 15 * 6)
print("Div: ", 15 / 6)
print("Floor Div: ", 15 // 6)
print("Rem: ", 15 % 6)
print("exp: ", 5 ** 6)